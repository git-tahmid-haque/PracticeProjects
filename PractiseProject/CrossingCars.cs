using System;
using System.Collections.Generic;
using System.Text;

namespace PractiseProject
{
    class CrossingCars
    {
        public static int NumberOfCarsCrossed(int[] carSequence)
        {
            int counter = 0;

            foreach (int car in carSequence)
            {
                if (car == 1)
                {
                    continue;
                }







            }







            return counter;
        }
    }
}
